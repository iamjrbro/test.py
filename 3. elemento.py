# a) 1, 3, 5, 7, ___
# Lógica: Adicionar 2 ao número anterior
# Próximo elemento: 9

print("Próximo elemento da sequência a): ", 7+2)


# b) 2, 4, 8, 16, 32, 64, ____
# Lógica: Multiplicar por 2 o número anterior
# Próximo elemento: 128

print("Próximo elemento da sequência b): ", 64*2)


# c) 0, 1, 4, 9, 16, 25, 36, ____
# Lógica: Somar 1 ao quadrado do número anterior
# Próximo elemento: 49

print("Próximo elemento da sequência c): ", 36+1+2*6)


# d) 4, 16, 36, 64, ____
# Lógica: Adicionar 4 ao quadrado do número anterior
# Próximo elemento: 100

print("Próximo elemento da sequência d): ", 64+4+2*7)


# e) 1, 1, 2, 3, 5, 8, ____
# Lógica: Somar os dois números anteriores
# Próximo elemento: 13

print("Próximo elemento da sequência e): ", 8+5)